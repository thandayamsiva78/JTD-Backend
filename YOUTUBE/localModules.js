

// Local Modules is a piece of code  we can create our own methods....

//Create Own Modules


function add (a , b) {
    return (a + b)
}

function substract (a , b) {
    return (a - b)
}


function multiply (a , b) {
    return (a * b)
}


function division (a , b) {
    return (a / b)
}



module.exports = {add , substract , multiply , division}  // these are local modules
